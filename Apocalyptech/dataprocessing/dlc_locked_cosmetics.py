                # Get the english name for it, too, if we can
                label = cust
                if 'InventoryData' in export and 'export' in export['InventoryData']:
                    invdata = obj_data[export['InventoryData']['export']-1]
                    if 'InventoryName' in invdata and 'string' in invdata['InventoryName']:
                        label = '{} ({})'.format(
                                cust,
                                invdata['InventoryName']['string'],
                                )

                print('{}: {}'.format(label, lock))
                break

